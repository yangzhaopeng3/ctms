package mainactivity.hall;

public class HallInfo {
    private int hallNo;
    private int hallSeat;
    private int isWork;

    public int getHallNo() {
        return hallNo;
    }

    public void setHallNo(int hallNo) {
        this.hallNo = hallNo;
    }

    public int getHallSeat() {
        return hallSeat;
    }

    public void setHallSeat(int hallSeat) {
        this.hallSeat = hallSeat;
    }

    public int getIsWork() {
        return isWork;
    }

    public void setIsWork(int isWork) {
        this.isWork = isWork;
    }
}
